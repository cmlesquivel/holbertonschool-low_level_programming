#include "3-calc.h"

/**
 * op_add - returns the sum
 *
 * @a: numer one
 * @b: number two
 * Return: the add
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the sub
 *
 * @a: numer one
 * @b: number two
 * Return: the sub
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - returns the mul
 *
 * @a: numer one
 * @b: number two
 * Return: the mul
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the div
 *
 * @a: numer one
 * @b: number two
 * Return: the div
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the mod
 *
 * @a: numer one
 * @b: number two
 * Return: the mod
 */


int op_mod(int a, int b)
{
	return (a % b);
}
